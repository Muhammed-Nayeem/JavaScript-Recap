//Parameter & Arguments:
function add(a, b) {
  var sum = 0;
  sum = a + b;
  console.log("Sum is: "+ sum);
}
add(10, 20);

function sub(a, b) {
  var sub = 0;
  sub = a - b;
  console.log("Sub is: "+ sub);
}
sub(20, 10);

function mul(a, b) {
  var mul = 0;
  mul = a * b;
  console.log("Multiplication is: "+ mul);
}
mul(5, 6);
